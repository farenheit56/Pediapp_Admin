export default {
  name: 'DrawerOptions',
  props: {
    title: {
      type: String,
      required: true
    },

    link: {
      type: String,
      default: '#'
    },

    icon: {
      type: String,
      default: ''
    }
  },
  methods: {
    changeSection(link) {
      this.$router.push({ path: `${link}` })
    }
  }
}
</script>
